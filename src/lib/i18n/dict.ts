export function getTranslation(lang: Lang, path: string) {
  const keys = path.split('.');
  let result: any = DICTIONARY[lang] || DICTIONARY['vi'];
  for (const key of keys) {
    if (result[key] === undefined) return path;
    result = result[key];
  }
  return result as string;
}
